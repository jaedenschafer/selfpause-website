/**
 * Match a user query to relevant category tags
 */
function matchCategories(query) {
  const normalized = query.toLowerCase().trim();
  const matchedTags = new Set();

  for (const category of CATEGORIES) {
    // Check if any keyword appears in the query
    for (const keyword of category.keywords) {
      if (normalized.includes(keyword)) {
        matchedTags.add(category.id);
        break;
      }
    }
    // Also check if the category label appears in the query
    if (normalized.includes(category.label.toLowerCase())) {
      matchedTags.add(category.id);
    }
  }

  // If no direct match, try individual word matching
  if (matchedTags.size === 0) {
    const words = normalized.split(/\s+/);
    for (const word of words) {
      if (word.length < 3) continue;
      for (const category of CATEGORIES) {
        for (const keyword of category.keywords) {
          if (keyword.includes(word) || word.includes(keyword)) {
            matchedTags.add(category.id);
          }
        }
      }
    }
  }

  return Array.from(matchedTags);
}

/**
 * Shuffle an array using Fisher-Yates algorithm
 */
function shuffle(array) {
  const arr = [...array];
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

/**
 * Get affirmations based on a query string
 * @param {string} query - User's input topic
 * @param {number} count - Number of affirmations to return (default 25)
 * @returns {{ affirmations: Array, matchedCategories: Array }}
 */
export function getAffirmations(query, count = 25) {
  const matchedTags = matchCategories(query);

  let filtered;
  if (matchedTags.length > 0) {
    // Get all affirmations that have at least one matching tag
    filtered = AFFIRMATIONS.filter(a =>
      a.tags.some(tag => matchedTags.includes(tag))
    );

    // Score them — more matching tags = higher priority
    filtered = filtered.map(a => ({
      ...a,
      score: a.tags.filter(tag => matchedTags.includes(tag)).length
    }));

    // Sort by score descending, then shuffle within same score
    filtered.sort((a, b) => b.score - a.score);

    // Take top results but shuffle to add variety
    const topTier = filtered.filter(a => a.score === filtered[0]?.score);
    const rest = filtered.filter(a => a.score < filtered[0]?.score);

    filtered = [...shuffle(topTier), ...shuffle(rest)];
  } else {
    // No match — return random affirmations
    filtered = shuffle(AFFIRMATIONS);
  }

  return {
    affirmations: filtered.slice(0, count),
    matchedCategories: matchedTags.map(id => CATEGORIES.find(c => c.id === id)).filter(Boolean),
  };
}

/**
 * Get affirmations by specific category ID
 */
export function getAffirmationsByCategory(categoryId, count = 25) {
  const filtered = AFFIRMATIONS.filter(a => a.tags.includes(categoryId));
  return {
    affirmations: shuffle(filtered).slice(0, count),
    matchedCategories: [CATEGORIES.find(c => c.id === categoryId)].filter(Boolean),
  };
}
